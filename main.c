#include <stdlib.h>
#include <stdio.h>

#include "log.h"
#include "cpu.h"
#include "mem.h"
#include "scr.h"

uint16_t program[] =
{
	0x0E00,		// cls
	0x0a61,		// set v1 a
	0x29f1,		// load system sprite vx

	0x0062,		// set v1 a
	0x0063,		// set v1 a
	0x25D3		// draw (2,3) 5
};

// these fonts are basically numbers lol
uint8_t sprites[] = 
{
	0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
	0x20, 0x60, 0x20, 0x20, 0x70, // 1
	0xF0, 0x80, 0xF0, 0x10, 0xF0, // 2
	0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
	0x90, 0x90, 0xF0, 0x10, 0x10, // 4
	0xF0, 0x10, 0xF0, 0x80, 0xF0, // 5
	0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
	0xF0, 0x10, 0x20, 0x40, 0x40, // 7
	0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
	0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
	0xF0, 0x90, 0xF0, 0x90, 0x90, // A
	0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
	0xF0, 0x80, 0x80, 0x80, 0xF0, // C
	0xE0, 0x90, 0x90, 0x90, 0xE0, // D
	0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
	0xF0, 0x80, 0xF0, 0x80, 0x80  // F
};

int main(void)
{
	mem_t ram = {
		.data = calloc(1, 0x1000),
		.len = 0x1000
	}; 

	mem_save(&ram, 0x200, (uint8_t*)program, sizeof program);
	mem_save(&ram, SPRITE_BEG, sprites, sizeof sprites);

	scr_t scr = scr_init(0x3f, 0x1f);

	cpu_t c = {
		.v = {0},
		.i = 0,
		.pc = 0x200,
		.sp = 0xff, // cause why not ;-)
		.dt = 0,
		.st = 0,

		.mem = &ram,
		.scr = &scr
	};

	int running = 1;

	while(running)
	{
		running = scr_update(&scr);

		cpu_prnt_status(&c);
		uint16_t op = cpu_fetch(&c);
		cpu_execute(&c, op);
	}

	scr_dinit(&scr);
}
